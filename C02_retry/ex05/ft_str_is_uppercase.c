int	ft_str_is_uppercase(char *str)
{
	int	i;

	if (*str == '\0')
	{
		i = 1;
	}
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			i = 1;
		}
		else
		{
			return (0);
		}
		str++;
	}
	return (i);
}
